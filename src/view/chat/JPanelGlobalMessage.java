    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String messageContent = jTextArea1.getText().trim();

        if (messageContent.isEmpty()) {
            JOptionPane.showMessageDialog(
                    null,
                    "Please provide content for message!",
                    constant.ConstantMessages.ERROR_MSG_TITLE,
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            Controller.getController().sendGlobalMessage(messageContent);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(
                    null,
                    "Error while trying to send message!!!!",
                    constant.ConstantMessages.ERROR_MSG_TITLE,
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


